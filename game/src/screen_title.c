// Title Screen Draw logic
void DrawTitleScreen(void)
{
    DrawRectangle(0, 0, GetScreenWidth(), GetScreenHeight(), BLUE);
    Vector2 pos = { 20, 10 };
    DrawTextEx(font, "Herding Cats", pos, font.baseSize*3.0f, 4, DARKGREEN);
    DrawText("PRESS ENTER or TAP to JUMP to Start Game", 120, 400, 20, DARKGREEN);

    int mainLogoX;// = 10;//((int)GetScreenWidth() - mainLogo.width) / 2;
    int mainLogoY;// = 220;
    mainLogoX = GetScreenWidth() / 2 - mainTexture.width / 2;
    mainLogoY = GetScreenHeight() / 2 - mainTexture.height / 2;
    DrawTexture(mainTexture, mainLogoX, mainLogoY, WHITE);
}

